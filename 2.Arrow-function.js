// Arrow function

let sumof = (a,b)=>{
    return a+b;
}
console.log(sumof(3,3));

// area of circle pi r
let areaofcircle = (r)=>{
    return 3.14*r;
}
let result = areaofcircle(2);
console.log(result);

// curcum of circle 2 pi r 
let cum = (r)=>{
    return 2*3.14*r;
}
let cumresult = cum(4);
console.log(cumresult);
